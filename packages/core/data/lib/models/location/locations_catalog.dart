import 'package:data/models/location/location_model.dart';

class LocationsCatalog {
  final double version;
  LocationsCatalog({this.version = 1.0, this.locationsCatalog = const []});
  final List<LocationModel> locationsCatalog;
  Map<String, dynamic> toJson() {
    return {
      "version": version,
      'locations': locationsCatalog
          .map((location) => location.toJson())
          .toList(),
    };
  }

  factory LocationsCatalog.fromJson(Map<String, dynamic> json) {
    return LocationsCatalog(
      version: json['version'],
      locationsCatalog: (json['locations'] as List<dynamic>)
          .map(
            (locationJson) =>
                LocationModel.fromJson(locationJson as Map<String, dynamic>),
          )
          .toList(),
    );
  }
}

