class CarCatalogModel {
  final double version;
  final List<CarBrand> brands;
  const CarCatalogModel({required this.version, required this.brands});
  Map<String, dynamic> toJson() {
    return {
      'version': version,
      'brands': brands.map((brand) => brand.toJson()).toList(),
    };
  }

  factory CarCatalogModel.fromJson(Map<String, dynamic> json) {
    return CarCatalogModel(
      version: json['version'] ?? 1.0,
      brands:
          (json['brands'] as List<dynamic>?)
              ?.map((brandJson) => CarBrand.fromJson(brandJson))
              .toList() ??
          [],
    );
  }
}
